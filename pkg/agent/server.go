// EventProcessor is a callback interface for processing k8s events in real-time.
// Stellar implements this to integrate with the console's event pipeline.
type EventProcessor interface {
	ProcessEvent(ctx context.Context, cluster, namespace, name, kind, reason, message, eventType string, count int32)
}

// Config holds agent configuration
type Config struct {
	Port           int
	Kubeconfig     string
	AllowedOrigins []string // Additional allowed origins (from --allowed-origins flag)
}

// AllowedOrigins for WebSocket connections (can be extended via env var)
var defaultAllowedOrigins = []string{
	"http://localhost",
	"https://localhost",
	"http://127.0.0.1",
	"https://127.0.0.1",
	// Known deployment URLs
	"https://console.kubestellar.io",
	"http://console.kubestellar.io",
	// Wildcard: any *.ibm.com subdomain (OpenShift routes, etc.)
	"https://*.ibm.com",
	"http://*.ibm.com",
}

// wsClient wraps a WebSocket connection with a per-connection write mutex
// to prevent gorilla/websocket panics from concurrent writes without
// requiring a global lock across all clients.
type wsClient struct {
	writeMu sync.Mutex
}

// Server is the local agent WebSocket server
type Server struct {
	config         Config
	upgrader       websocket.Upgrader
	kubectl        *KubectlProxy
	k8sClient      *k8s.MultiClusterClient // For rich cluster data queries
	registry       *Registry
	clients        map[*websocket.Conn]*wsClient
	clientsMux     sync.RWMutex
	allowedOrigins []string
	agentToken     string // Optional shared secret for authentication
	tokenExplicit  bool   // true when KC_AGENT_TOKEN was explicitly set (not auto-generated)

	// Token tracking
	tokenMux          sync.RWMutex
	tokenFileMux      sync.Mutex  // serializes file I/O in saveTokenUsage to prevent race (#9441)
	tokenFlushTimer   *time.Timer // debounced flush timer for batched disk writes (#9483)
	sessionStart      time.Time
	sessionTokensIn   int64
	sessionTokensOut  int64
	todayTokensIn     int64
	todayTokensOut    int64
	todayDate         string // YYYY-MM-DD format to detect day change
	lastSavedIn       int64  // todayTokensIn at last saveTokenUsage, for delta computation (#9730)
	lastSavedOut      int64  // todayTokensOut at last saveTokenUsage, for delta computation (#9730)
	sessionTokenQuota int64  // max total tokens per session; 0 = unlimited (#9438)

	// Prediction system
	predictionWorker *PredictionWorker
	metricsHistory   *MetricsHistory

	// Insight enrichment
	insightWorker *InsightWorker

	// Hardware device tracking
	deviceTracker *DeviceTracker

	// Local cluster management
	localClusters *LocalClusterManager
	clusterOpsWG  sync.WaitGroup // tracks in-flight cluster create/delete/lifecycle goroutines

	// Backend process management (for restart-from-UI)
	backendCmd *exec.Cmd
	backendMux sync.Mutex

	// activeChatEntry binds an in-progress session's cancel function to the
	// WebSocket connection that started it (#9712). handleCancelChat uses the
	// conn pointer to verify the requester owns the session before cancelling,
	// preventing cross-session CSRF/bypass attacks.
	activeChatCtxs   map[string]activeChatEntry
	activeChatCtxsMu sync.Mutex

	// dryRunSessions tracks session IDs that are in dry-run mode.
	// When a session is in this set, the kubectl proxy rejects mutating
	// commands for that session, enforcing dry-run at the server level
	// regardless of what the AI agent decides to do. (#6442)
	dryRunSessions   map[string]bool
	dryRunSessionsMu sync.RWMutex

	// Auto-update system
	updateChecker *UpdateChecker

	resourceRetryMu    sync.Mutex
	resourceRetryState map[string]clusterResourceRetryState

	// digestSequence tracks state integrity packets (#12000)
	digestSequence atomic.Int64
	stopCh         chan struct{}
	stopOnce       sync.Once

	SkipKeyValidation bool // For testing purposes

	// Event processor callback for Stellar integration
	eventProcessor EventProcessor

	// HTTP client for Stellar event forwarding (reused to avoid per-call allocation)
	stellarClient *http.Client

	// Semaphore for bounding concurrent event forwards to prevent goroutine exhaustion (#13991)
	stellarForwardSem       chan struct{}
	missionExecutionTimeout time.Duration
}

// NewServer creates a new agent server
func NewServer(cfg Config) (*Server, error) {
	kubectl, err := NewKubectlProxy(cfg.Kubeconfig)
	if err != nil {
		return nil, fmt.Errorf("failed to initialize kubectl proxy: %w", err)
	}

	// Initialize k8s client for rich cluster data queries
	k8sClient, err := k8s.NewMultiClusterClient(cfg.Kubeconfig)
	if err != nil {
		slog.Error("failed to initialize k8s client", "error", err)
		// Don't fail - kubectl functionality still works
	}

	SetClusterContextProviders(nil, k8sClient)

	// Initialize AI providers
	if err := InitializeProviders(); err != nil {
		slog.Warn("provider initialization issue", "error", err)
		// Don't fail - kubectl functionality still works without AI
	}

	// Build allowed origins list
	allowedOrigins := append([]string{}, defaultAllowedOrigins...)

	// Add custom origins from environment variable (comma-separated)
	if extraOrigins := os.Getenv("KC_ALLOWED_ORIGINS"); extraOrigins != "" {
		for _, origin := range strings.Split(extraOrigins, ",") {
			origin = strings.TrimSpace(origin)
			if origin != "" {
				allowedOrigins = append(allowedOrigins, origin)
			}
		}
	}

	// Add custom origins from CLI flag
	for _, origin := range cfg.AllowedOrigins {
		origin = strings.TrimSpace(origin)
		if origin != "" {
			allowedOrigins = append(allowedOrigins, origin)
		}
	}

	// Log non-default origins so users can verify their configuration
	if len(allowedOrigins) > len(defaultAllowedOrigins) {
		slog.Info("custom allowed origins configured", "origins", allowedOrigins[len(defaultAllowedOrigins):])
	}

	// Shared secret for authentication. When KC_AGENT_TOKEN is not set,
	// auto-generate a random token so the agent is never unauthenticated
	// by default (#9480).
	agentToken := os.Getenv("KC_AGENT_TOKEN")
	tokenExplicit := agentToken != ""
	generatedTokenFlag := os.Getenv("KC_AGENT_TOKEN_GENERATED")
	startupGeneratedToken := generatedTokenFlag == "1" || strings.EqualFold(generatedTokenFlag, "true")
	if tokenExplicit && startupGeneratedToken {
		tokenExplicit = false
		slog.Info("Agent token set (startup-generated) — origin-based bypass limited to safe status probes")
	} else if tokenExplicit {
		slog.Info("Agent token authentication enabled (from KC_AGENT_TOKEN)")
	} else {
		generated, err := generateRandomToken(autoGeneratedTokenBytes)
		if err != nil {
			return nil, fmt.Errorf("failed to generate random agent token: %w", err)
		}
		agentToken = generated
		slog.Warn("KC_AGENT_TOKEN is not set — auto-generated a random token for this session; set KC_AGENT_TOKEN to pin a shared secret (docs: https://github.com/kubestellar/console#kc-agent-bridge-self-hosted-console-to-your-clusters)")
		// Print to stderr so the operator can copy-paste it into clients.
		fmt.Fprintf(os.Stderr, "Auto-generated KC_AGENT_TOKEN for this session: %s\n", agentToken)                                                                                                             //nolint:forbidigo // intentional stderr for operator UX
		fmt.Fprintln(os.Stderr, "Set KC_AGENT_TOKEN in your shell or .env to use a persistent shared secret. See https://github.com/kubestellar/console#kc-agent-bridge-self-hosted-console-to-your-clusters") //nolint:forbidigo // intentional stderr for operator UX
	}

	// Resolve per-session token quota from env, falling back to the compiled
	// default. 0 means unlimited (operator opted out of the safety net).
	sessionQuota := defaultSessionTokenQuota
	if raw := os.Getenv(sessionTokenQuotaEnvVar); raw != "" {
		if parsed, err := strconv.ParseInt(raw, 10, 64); err == nil && parsed >= 0 {
			sessionQuota = parsed
		} else {
			slog.Warn("invalid KC_SESSION_TOKEN_QUOTA value, using default",
				"value", raw, "default", defaultSessionTokenQuota)
		}
	}
	if sessionQuota > 0 {
		slog.Info("session token quota enabled", "limit", sessionQuota)
	}

	// Resolve mission execution timeout from env, falling back to the
	// compiled default (#9482).
	missionExecutionTimeout := defaultMissionExecutionTimeout
	if raw := os.Getenv(missionTimeoutEnvVar); raw != "" {
		if parsed, err := time.ParseDuration(raw); err == nil && parsed > 0 {
			missionExecutionTimeout = parsed
			slog.Info("mission execution timeout overridden", "timeout", missionExecutionTimeout)
		} else {
			slog.Warn("invalid KC_MISSION_TIMEOUT value, using default",
				"value", raw, "default", defaultMissionExecutionTimeout)
		}
	}

	now := time.Now()
	server := &Server{
		config:                  cfg,
		kubectl:                 kubectl,
		k8sClient:               k8sClient,
		registry:                GetRegistry(),
		clients:                 make(map[*websocket.Conn]*wsClient),
		allowedOrigins:          allowedOrigins,
		agentToken:              agentToken,
		tokenExplicit:           tokenExplicit,
		sessionStart:            now,
		todayDate:               now.Format("2006-01-02"),
		activeChatCtxs:          make(map[string]activeChatEntry),
		dryRunSessions:          make(map[string]bool),
		resourceRetryState:      make(map[string]clusterResourceRetryState),
		sessionTokenQuota:       sessionQuota,
		missionExecutionTimeout: missionExecutionTimeout,
		stellarClient: &http.Client{
			Timeout: 5 * time.Second,
			Transport: &http.Transport{
				MaxIdleConnsPerHost: 2,
			},
		},
		stellarForwardSem: make(chan struct{}, 32), // maxConcurrentForwards from server_events_stream.go
	}

	server.upgrader = websocket.Upgrader{
		CheckOrigin: server.checkOrigin,
	}

	// Load persisted token usage from disk
	server.loadTokenUsage()

	// Initialize prediction system
	server.predictionWorker = NewPredictionWorker(k8sClient, server.registry, server.BroadcastToClients, server.addTokenUsage)
	server.metricsHistory = NewMetricsHistory(k8sClient, "")

	// Initialize insight enrichment
	server.insightWorker = NewInsightWorker(server.registry, server.BroadcastToClients)

	// Initialize local cluster manager with broadcast callback for progress updates
	server.localClusters = NewLocalClusterManager(server.BroadcastToClients)

	// Initialize auto-update checker
	server.updateChecker = NewUpdateChecker(UpdateCheckerConfig{
		Broadcast:      server.BroadcastToClients,
		RestartBackend: server.startBackendProcess,
		KillBackend:    server.killBackendProcess,
	})

	// Initialize device tracker with notification callback
	server.deviceTracker = NewDeviceTracker(k8sClient, func(msgType string, payload interface{}) {
		server.BroadcastToClients(msgType, payload)
		// Send native notification for device alerts
		if msgType == "device_alerts_updated" {
			if resp, ok := payload.(DeviceAlertsResponse); ok && len(resp.Alerts) > 0 {
				server.sendNativeNotification(resp.Alerts)
			}
		}
	})

	server.stopCh = make(chan struct{})

	// Start state integrity worker (#12000)
	safego.GoWith("server/state-digest-worker", server.startStateDigestWorker)

	return server, nil
}

// SetEventProcessor sets the event processor callback for Stellar integration.
// This allows the agent to forward k8s events to Stellar's notification system.
func (s *Server) SetEventProcessor(processor EventProcessor) {
	s.eventProcessor = processor
}

// Start starts the agent server
func (s *Server) Start() error {
	mux := http.NewServeMux()

	s.registerRoutes(mux)

	// Wrap the mux with CSRF middleware: state-changing requests (POST, PUT,
	// DELETE, PATCH) must include the X-Requested-With: XMLHttpRequest header.
	// GET/HEAD/OPTIONS pass through unconditionally. This mirrors the Fiber
	// middleware in pkg/api/middleware/csrf.go. See #10000.
	//
	// #10699: corsMiddleware runs outermost so that CORS headers are present
	// on every response, including CSRF rejections. Without this layer the
	// browser reports an opaque CORS error instead of showing the 403.
	// corsMiddleware also short-circuits OPTIONS preflight so it never
	// reaches requireCSRF (preflight must not carry X-Requested-With).
	handler := s.corsMiddleware(maxBodyMiddleware(requireCSRF(mux)))

	addr := fmt.Sprintf("127.0.0.1:%d", s.config.Port)
	slog.Info("KC Agent starting", "version", Version, "addr", addr)
	slog.Info("health endpoint available", "url", "http://"+addr+"/health")
	slog.Info("WebSocket endpoint available", "url", "ws://"+addr+"/ws")

	// Validate all configured API keys on startup (run in background to not delay startup)
	safego.GoWith("validate-all-keys", func() { s.ValidateAllKeys() })

	// Start kubeconfig file watcher (uses k8s client's built-in watcher)
	if s.k8sClient != nil {
		s.k8sClient.SetOnReload(func() {
			slog.Info("[Server] Kubeconfig reloaded, broadcasting to clients...")
			s.kubectl.Reload()
			clusters, current := s.kubectl.ListContexts()
			s.BroadcastToClients("clusters_updated", protocol.ClustersPayload{
				Clusters: clusters,
				Current:  current,
			})
			slog.Info("[Server] broadcasted clusters to clients", "count", len(clusters))
		})
		if err := s.k8sClient.StartWatching(); err != nil {
			slog.Error("failed to start kubeconfig watcher", "error", err)
		}
	}

	// Start prediction system
	if s.predictionWorker != nil {
		s.predictionWorker.Start()
		slog.Info("Prediction worker started")
	}
	if s.metricsHistory != nil {
		s.metricsHistory.Start(metricsHistoryTick)
		slog.Info("Metrics history started")
	}

	// Start device tracker
	if s.deviceTracker != nil {
		s.deviceTracker.Start()
		slog.Info("Device tracker started")
	}

	// Load auto-update config from settings and start if enabled
	if s.updateChecker != nil {
		mgr := settings.GetSettingsManager()
		if all, err := mgr.GetAll(); err == nil && all.AutoUpdateEnabled {
			channel := all.AutoUpdateChannel
			if channel == "" {
				channel = "stable"
			}
			s.updateChecker.Configure(true, channel)
			slog.Info("auto-update started", "channel", channel)
		}
	}

	// Use explicit timeouts to prevent Slowloris-style DoS attacks (#7262).
	const (
		serverReadHeaderTimeout = 10 * time.Second
		serverReadTimeout       = 30 * time.Second
		serverWriteTimeout      = 60 * time.Second
		serverIdleTimeout       = 120 * time.Second
	)
	srv := &http.Server{
		Addr:              addr,
		Handler:           handler,
		ReadHeaderTimeout: serverReadHeaderTimeout,
		ReadTimeout:       serverReadTimeout,
		WriteTimeout:      serverWriteTimeout,
		IdleTimeout:       serverIdleTimeout,
	}
	return srv.ListenAndServe()
}
